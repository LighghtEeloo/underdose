_underdose() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="underdose"
                ;;
            underdose,config)
                cmd="underdose__config"
                ;;
            underdose,drain)
                cmd="underdose__drain"
                ;;
            underdose,help)
                cmd="underdose__help"
                ;;
            underdose,init)
                cmd="underdose__init"
                ;;
            underdose,pour)
                cmd="underdose__pour"
                ;;
            underdose,sync)
                cmd="underdose__sync"
                ;;
            underdose,where)
                cmd="underdose__where"
                ;;
            underdose__help,config)
                cmd="underdose__help__config"
                ;;
            underdose__help,drain)
                cmd="underdose__help__drain"
                ;;
            underdose__help,help)
                cmd="underdose__help__help"
                ;;
            underdose__help,init)
                cmd="underdose__help__init"
                ;;
            underdose__help,pour)
                cmd="underdose__help__pour"
                ;;
            underdose__help,sync)
                cmd="underdose__help__sync"
                ;;
            underdose__help,where)
                cmd="underdose__help__where"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        underdose)
            opts="-c -h -V --config --help --version init config where drain sync pour help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --config)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                -c)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        underdose__config)
            opts="-h --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        underdose__drain)
            opts="-s -h --store --help"
